package com.vijay.stringmethods;

public class SubString {
	public static void main(String[] args) {
		String str = "i am from solapur";
		System.out.println("original string str : "+str);
		System.out.println("sub string of original string is : -->"+str.substring(4));
		System.out.println("sub string IN BETWEEN of original string is : -->"+str.substring(2, 9));
	}

}
